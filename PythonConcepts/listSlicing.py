a = [1, 2, 3, 4, 5, 6, 7, 8, 9]

# Get elements starting from index 2
# to the end of the list
b = a[2:]
print(b)

# Get elements starting from index 0 
# to index 3 (excluding 3th index)
c = a[:3]
print(c)

b = a[1:4]
print(b)

# Get every third element from the list
# starting from index 1 to 8(exclusive)
c = a[1:8:3]
print(c)